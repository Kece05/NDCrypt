// Cooley-Tukey DIT NTT (in-place)
fn ntt_dit(a: &mut [u16; N], twiddles: &[u16; 1023]) {
    // Bit-reversal permutation (log2(N) = 10 bits)
    let log_n: u32 = 10;
    for i in 0..N {
        let j = (i as u32).reverse_bits() >> (32 - log_n);
        let j = j as usize;
        if i < j {
            a.swap(i, j);
        }
    }

    let q = Q as u32;
    let mut tw_idx = 0usize;
    let mut length = 1usize;

    while length < N {
        // All N/(2·length) blocks reuse the same `length` twiddles
        for start in (0..N).step_by(2 * length) {
            for j in 0..length {
                let w = twiddles[tw_idx + j] as u32;
                let u = a[start + j] as u32;
                let v = (w * a[start + length + j] as u32) % q;
                a[start + j]          = ((u + v)     % q) as u16;
                a[start + length + j] = ((u + q - v) % q) as u16;
            }
        }
        tw_idx += length; // advance once per stage, not once per block
        length *= 2;
    }
}

// Forward negacyclic NTT
fn ntt_forward(a: &[u16; N]) -> [u16; N] {
    let q = Q as u32;
    let mut out = [0u16; N];
    // Pre-twist: out[i] = a[i] * psi^i  (mod Q)
    for i in 0..N {
        out[i] = ((a[i] as u32 * PSI_POWERS[i] as u32) % q) as u16;
    }
    ntt_dit(&mut out, &TWIDDLES_FWD);
    return out;
}

// Inverse negacyclic NTT
fn ntt_inverse(a: &[u16; N]) -> [u16; N] {
    let q = Q as u32;
    let mut out = *a;
    ntt_dit(&mut out, &TWIDDLES_INV);
    // Scale by N^{-1} and post-untwist: out[i] = out[i] * N_inv * psi_inv^i  (mod Q)
    let n_inv = N_INV as u32;
    for i in 0..N {
        out[i] = ((out[i] as u32 * n_inv % q) * PSI_INV_POWERS[i] as u32 % q) as u16;
    }
    return out;
}


#[derive(Clone, Zeroize, ZeroizeOnDrop)]
pub struct RingElement {
    pub coeffs: [u16; N],
}

/// Samples a uniform polynomial over Z_q where each coefficient is in 0..Q.
pub fn fill_ring_element(rng: &mut impl RngCore) -> RingElement {
    let mut element = RingElement { coeffs: [0u16; N] };

    // Rejection-sample with a 14-bit mask to avoid modular skew.
    const BIT_MASK: u32 = 0x3FFF;
    for i in 0..N {
        loop {
            let candidate = rng.next_u32() & BIT_MASK;
            if candidate < Q as u32 {
                element.coeffs[i] = candidate as u16;
                break;
            }
        }
    }

    element
}

// Samples a small noise polynomial from the centred binomial distribution
pub fn sample_small(rng: &mut impl RngCore) -> RingElement {
    let mut element = [0u16; N];
    let q = Q as u32;

    for i in 0..(N / 8) {
        let noise = rng.next_u32();
        for j in 0..8 {
            let chunk = (noise >> (j * 4)) & 0x0F;
            let a = (chunk & 1) + ((chunk >> 1) & 1);
            let b = ((chunk >> 2) & 1) + ((chunk >> 3) & 1);
            element[i * 8 + j] = ((a + q - b) % q) as u16;
        }
    }

    RingElement { coeffs: element }
}

// Adds two ring elements coefficient-wise mod Q
pub fn ring_add(a_co: &RingElement, b_co: &RingElement) -> RingElement {
    let mut new_ring = [0u16; N];
    for i in 0..N {
        new_ring[i] = (a_co.coeffs[i] + b_co.coeffs[i]) % Q;
    }
    RingElement { coeffs: new_ring }
}

// Subtracts two ring elements coefficient-wise mod Q.
pub fn ring_subtract(a_co: &RingElement, b_co: &RingElement) -> RingElement {
    let mut new_ring = [0u16; N];
    for i in 0..N {
        new_ring[i] = (a_co.coeffs[i] + Q - b_co.coeffs[i]) % Q;
    }
    RingElement { coeffs: new_ring }
}

// Multiplies two polynomials in Z_q[x]/(x^N + 1) using the negacyclic NTT
// The 3-step process:
//   1. Forward NTT both operands (pre-twist + DIT cyclic NTT)
//   2. Pointwise multiply in the NTT domain
//   3. Inverse NTT (DIT cyclic NTT + N^{-1} scale + post-untwist)
pub fn ring_multiply(a_co: &RingElement, b_co: &RingElement) -> RingElement {
    let q = Q as u32;
    let a_ntt = ntt_forward(&a_co.coeffs);
    let b_ntt = ntt_forward(&b_co.coeffs);

    let mut c_ntt = [0u16; N];
    for i in 0..N {
        c_ntt[i] = ((a_ntt[i] as u32 * b_ntt[i] as u32) % q) as u16;
    }

    RingElement { coeffs: ntt_inverse(&c_ntt) }
}



// Encodes a 32-byte seed into a polynomial — each bit mapped to 0 or Q/2
pub fn encode_seed(seed: &[u8; 32]) -> RingElement {
    let mut coeffs = [0u16; N];
    for i in 0..32 {
        for j in 0..8 {
            let bit = (seed[i] >> j) & 1;
            coeffs[i * 8 + j] = if bit == 1 { 6144 } else { 0 };
        }
    }
    RingElement { coeffs }
}

// Decodes a noisy polynomial back into a 32-byte seed.
// A coefficient is decoded as 1 if it falls in the range [Q/4, 3Q/4)
pub fn decode_seed(element: &RingElement) -> [u8; 32] {
    let mut seed = [0u8; 32];
    for i in 0..32 {
        for j in 0..8 {
            let coeff = element.coeffs[i * 8 + j];
            let low:  Choice = !coeff.ct_lt(&3072_u16);
            let high: Choice = coeff.ct_lt(&9216_u16);
            let is_one = u8::conditional_select(&0u8, &1u8, low & high);
            seed[i] |= is_one << j;
        }
    }
    seed
}

// Derives 32 signal coordinate indices (S) from the shared seed and a nonce
pub fn derive_signal_indices(seed: &[u8; 32], nonce: u64) -> [u16; SIGNAL_COUNT] {
    use sha3::{Digest, Sha3_256};
    use rand::SeedableRng;
    use rand::rngs::StdRng;
    use rand::seq::SliceRandom;

    let mut hasher = Sha3_256::new();
    hasher.update([0x01]);
    hasher.update(nonce.to_le_bytes());
    hasher.update(seed);
    let digest = hasher.finalize();

    let mut s_seed = [0u8; 32];
    s_seed.copy_from_slice(&digest);

    let mut prng = StdRng::from_seed(s_seed);
    let mut indices: Vec<u16> = (0..N as u16).collect();
    indices.shuffle(&mut prng);

    let mut signal_indices = [0u16; SIGNAL_COUNT];
    signal_indices.copy_from_slice(&indices[..SIGNAL_COUNT]);
    signal_indices.sort_unstable();

    signal_indices
}